package com.rahuldhanawade.chemcaliba.adapter;

public class PTMeetingPOJO {
    private Integer ptMeetingsMasterId;
    private String ptMeetingsTitle;
    private String ptMeetingsDateTime;
    private String createdDate;
    private String updatedDate;
    private String courseCategoryName;

    public Integer getPtMeetingsMasterId() {
        return ptMeetingsMasterId;
    }

    public void setPtMeetingsMasterId(Integer ptMeetingsMasterId) {
        this.ptMeetingsMasterId = ptMeetingsMasterId;
    }

    public String getPtMeetingsTitle() {
        return ptMeetingsTitle;
    }

    public void setPtMeetingsTitle(String ptMeetingsTitle) {
        this.ptMeetingsTitle = ptMeetingsTitle;
    }

    public String getPtMeetingsDateTime() {
        return ptMeetingsDateTime;
    }

    public void setPtMeetingsDateTime(String ptMeetingsDateTime) {
        this.ptMeetingsDateTime = ptMeetingsDateTime;
    }

    public String getCreatedDate() {
        return createdDate;
    }

    public void setCreatedDate(String createdDate) {
        this.createdDate = createdDate;
    }

    public String getUpdatedDate() {
        return updatedDate;
    }

    public void setUpdatedDate(String updatedDate) {
        this.updatedDate = updatedDate;
    }

    public String getCourseCategoryName() {
        return courseCategoryName;
    }

    public void setCourseCategoryName(String courseCategoryName) {
        this.courseCategoryName = courseCategoryName;
    }
}

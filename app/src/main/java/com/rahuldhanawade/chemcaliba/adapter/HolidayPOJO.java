package com.rahuldhanawade.chemcaliba.adapter;

public class HolidayPOJO {
    private Integer holidayInformationMasterId;
    private String holidayInformationTitle;
    private String holidayInformationFromDate;
    private String holidayInformationToDate;
    private String createdDate;
    private String updatedDate;
    private String courseCategoryName;

    public Integer getHolidayInformationMasterId() {
        return holidayInformationMasterId;
    }

    public void setHolidayInformationMasterId(Integer holidayInformationMasterId) {
        this.holidayInformationMasterId = holidayInformationMasterId;
    }

    public String getHolidayInformationTitle() {
        return holidayInformationTitle;
    }

    public void setHolidayInformationTitle(String holidayInformationTitle) {
        this.holidayInformationTitle = holidayInformationTitle;
    }

    public String getHolidayInformationFromDate() {
        return holidayInformationFromDate;
    }

    public void setHolidayInformationFromDate(String holidayInformationFromDate) {
        this.holidayInformationFromDate = holidayInformationFromDate;
    }

    public String getHolidayInformationToDate() {
        return holidayInformationToDate;
    }

    public void setHolidayInformationToDate(String holidayInformationToDate) {
        this.holidayInformationToDate = holidayInformationToDate;
    }

    public String getCreatedDate() {
        return createdDate;
    }

    public void setCreatedDate(String createdDate) {
        this.createdDate = createdDate;
    }

    public String getUpdatedDate() {
        return updatedDate;
    }

    public void setUpdatedDate(String updatedDate) {
        this.updatedDate = updatedDate;
    }

    public String getCourseCategoryName() {
        return courseCategoryName;
    }

    public void setCourseCategoryName(String courseCategoryName) {
        this.courseCategoryName = courseCategoryName;
    }
}

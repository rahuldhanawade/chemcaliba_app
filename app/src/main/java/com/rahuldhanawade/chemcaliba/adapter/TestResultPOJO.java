package com.rahuldhanawade.chemcaliba.adapter;

public class TestResultPOJO {
    private Integer testRecordsMasterId;
    private String testDate;
    private String marksObtained;
    private String totalMarks;
    private String noOfRightQuestions;
    private String noOfWrongQuestions;
    private String createdDate;
    private String isAttempted;

    public Integer getTestRecordsMasterId() {
        return testRecordsMasterId;
    }

    public void setTestRecordsMasterId(Integer testRecordsMasterId) {
        this.testRecordsMasterId = testRecordsMasterId;
    }

    public String getTestDate() {
        return testDate;
    }

    public void setTestDate(String testDate) {
        this.testDate = testDate;
    }

    public String getMarksObtained() {
        return marksObtained;
    }

    public void setMarksObtained(String marksObtained) {
        this.marksObtained = marksObtained;
    }

    public String getTotalMarks() {
        return totalMarks;
    }

    public void setTotalMarks(String totalMarks) {
        this.totalMarks = totalMarks;
    }

    public String getNoOfRightQuestions() {
        return noOfRightQuestions;
    }

    public void setNoOfRightQuestions(String noOfRightQuestions) {
        this.noOfRightQuestions = noOfRightQuestions;
    }

    public String getNoOfWrongQuestions() {
        return noOfWrongQuestions;
    }

    public void setNoOfWrongQuestions(String noOfWrongQuestions) {
        this.noOfWrongQuestions = noOfWrongQuestions;
    }

    public String getCreatedDate() {
        return createdDate;
    }

    public void setCreatedDate(String createdDate) {
        this.createdDate = createdDate;
    }

    public String getIsAttempted() {
        return isAttempted;
    }

    public void setIsAttempted(String isAttempted) {
        this.isAttempted = isAttempted;
    }

}

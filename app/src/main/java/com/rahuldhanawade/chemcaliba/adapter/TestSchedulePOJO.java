package com.rahuldhanawade.chemcaliba.adapter;

public class TestSchedulePOJO {
    private Integer testScheduleMasterId;
    private String testScheduleTitle;
    private String createdDate;
    private String courseCategoryName;
    private String testScheduleDateTime;
    private String testScheduleLink;

    public Integer getTestScheduleMasterId() {
        return testScheduleMasterId;
    }

    public void setTestScheduleMasterId(Integer testScheduleMasterId) {
        this.testScheduleMasterId = testScheduleMasterId;
    }

    public String getTestScheduleTitle() {
        return testScheduleTitle;
    }

    public void setTestScheduleTitle(String testScheduleTitle) {
        this.testScheduleTitle = testScheduleTitle;
    }

    public String getCreatedDate() {
        return createdDate;
    }

    public void setCreatedDate(String createdDate) {
        this.createdDate = createdDate;
    }

    public String getCourseCategoryName() {
        return courseCategoryName;
    }

    public void setCourseCategoryName(String courseCategoryName) {
        this.courseCategoryName = courseCategoryName;
    }

    public String getTestScheduleDateTime() {
        return testScheduleDateTime;
    }

    public void setTestScheduleDateTime(String testScheduleDateTime) {
        this.testScheduleDateTime = testScheduleDateTime;
    }

    public String getTestScheduleLink() {
        return testScheduleLink;
    }

    public void setTestScheduleLink(String testScheduleLink) {
        this.testScheduleLink = testScheduleLink;
    }

}
